# Working with strings

phrase = "Giraffe"
print(phrase.upper() + "\n\"Academy\"") # upper() and \ exit function
print(phrase.lower()) # lowercase function
print(phrase.isupper()) # check if word is uppercase
print(phrase.upper().isupper()) # combining uppercase with uppercase check function
print(len(phrase)) # length of string
print(phrase[0]) # index of character in string
print(phrase.index("G")) # find index of character
print(phrase.replace("fe", "ferig")) # replace string